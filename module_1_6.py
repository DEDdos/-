# 1
my_dict = {
    "Maria": 1995,
    "Artem": 2000,
    "Lada": 2003
}

# вывод словаря, существующей и несуществующей переменной
print("Словарь: ", my_dict)
print("Существующая переменная: ", my_dict.get("Maria"))
print("НЕсуществующая переменная: ",my_dict.get('Ivan'))

# добавляем новые значения в словарь
my_dict["Nikita"] = 2002
my_dict["Kirill"] = 2001

# выводим, а затем удаляем одну из пар в словаре по существующему ключу из словаря
print("Пара из словаря на удаление", my_dict["Artem"])
my_dict.pop("Artem")

# вывод изменённого словаря
print("Изменённый словарь: ", my_dict)

