# 2

my_set = {1, 2, 'Яблоко', 42.314, 1, 'Яблоко'}

# вывод уникальных переменных из словаря
print("Множество переменных: ", my_set)

# добавляем два новых элемента в множество
my_set.add(13)
my_set.add((5, 6, 1.6))

# удаляем один элемент из множества
my_set.discard(2)  # используем discard, чтобы не было ошибки, если элемента нет

# вывод изменённого словаря
print("Изменённое множество переменных: ", my_set)